	md"""
	## Creating Synthetic Data

	First, lets create a synthetic dataset. The purpose being to create controlled situations for testing the methods. We can create synthetic datasets that match the assumptions underlying a method, and so test our implementations. If they do not behave as we expect, then this might indicate a bug (conceptually or in our code).
	
	The synthetic data has targets $y$ that are generated by sampling from a Gaussian where the mean is a linear function $f$ of $\mathbf{x}$ with some fixed variance $\sigma^2$.
	
	To create a synthetic dataset, we need to decide what distribution to use. I have preffered to generate these inputs randomly from Gaussians.
	
