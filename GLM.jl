# ╔═╡ 1386bd89-a143-4084-981c-a77c76314455
num_samples = 30000

# ╔═╡ 04701d03-e8ff-4da1-8cf6-f77221785b0c
# Convenient to have number of features assigned to a variable
num_features = 15 + # base features 
	           1    # added column of ones

# ╔═╡ 95183b18-85ad-4460-b1bd-3199577a101f
"""
	generate_features(rng)

This function generates features according to a InverseGaussian distribution 
(μ = 0.1 and λ = 1).
"""
function generate_features(rng)
	μs = rand(rng, 10)
	X = reduce(hcat, [rand(rng, InverseGaussian(μs[i], 1), num_samples) for i in 1:10])

	# Now we can add some components which are linearly dependent
	X = hcat(X, 0.33*X[:, 1] .+  0.67*X[:, 5])
	X = hcat(X, -X[:, 4])
	X = hcat(X, X[:, 3] .- X[:, 8])
	X = hcat(X, X[:, 9] .+ 0.5*X[:, 10])
	X = hcat(X, X[:, 6] .- 0.5*X[:, 2])
	

	# let's also append a column of ones to act as a bias unit
	X = cat(X, ones(num_samples), dims=2)
	
	return X
end

# ╔═╡ 22709dd8-1426-46a8-af38-d1908d6333ca
md"
Below, we specify the linear function that we will use to generate the synthetic data. A few restrictions include:
1. Equation $f(x) = c$ for some constant $c$ cannot be permitted
2. Function $f(x)$ depends on at least 2 columns of $X$
3. function should be single variate (1 output)
"

# ╔═╡ d0f7c8ad-a7a1-4b8e-a8a5-5a1dabe30421
W_true = let
	w_temp = rand(Random.Xoshiro(10), Normal(0, 0.2), num_features, 1)
	w_temp
end

# ╔═╡ 065786f5-00a2-466b-b634-72b7aee33c18
# TODO: f should be a linear function, that uses the weights W_true
function f(X::AbstractMatrix)
	szc = size(X,2)
	szr = size(X,1)
	szc = 1
	output = 0
	szr = 5
	for t in (1:szc)
		the_weight = W_true[t,:][1]
		for j in (1:szr)
			new = X[j,:][szc] * the_weight
			output += new
		end
	end
	output
end

