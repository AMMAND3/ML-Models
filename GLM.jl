# ╔═╡ 076572bd-d51f-4bee-9621-fc5efe28310e
begin
	md"""
	### Simple Linear Regression
	
	As mentioned above, this implementation of linear regression (LR) using stochastic gradient descent (SGD) with one sample.
	
	Steps:-
	
	i) For each epoch, dataset is randomly shuffled.
	
	ii) When iterated over in order, update for each sample $i$.
	
	iii) A fixed scalar stepsize is used, rather than adaptive vector stepsizes.
	
	We opted for a simpler implementation of linear regression, where we do not use mini-batches.
	"""
end

# ╔═╡ ebfa9645-3108-4f49-a6b9-a7d213a7d857
begin
	mutable struct LinearModel <: Model
		W::Matrix
	end

	# add a default initializer (all weights=0)
	LinearModel(in, out=1) = LinearModel(
		# initialize W
		zeros(in, out)
	)

	# For consistency with notes, define inner-product with column vectors
	predict(lm::LinearModel, x::AbstractVector) = x'*lm.W
	# and with matrices
	predict(lm::LinearModel, X::AbstractMatrix) = X*lm.W
end

